<div data-role="page" id="blackdrie">
   <div data-role="header">
      <a href="#dialogPage_5" data-
         icon="back"></a>     
      <h1>Black Metal Derde Golf</h1>
   </div>
   <div data-role="content" >
      <div
         class="verplaatsing">
         <p>De derde golf is meer een mix van andere stijlen met black metal, en het
            kopiëren van de oudere bands.
            Veel bands kopieerden de sound van Burzum en Darkthrone; de minimalistische mid-paced black metal.
            Zij kent een grote scene in Frankrijk, met bands als Antaeus, Arkhon Infaustus, Deathspell Omega,
            Blut Aus Nord; in Oekraïne met bands als Lucifugum en Nokturnal Mortum; in Duitsland met bands als
            Nargaroth, Katharsis, Odal en Ulfsdalir.
         </p>
         <p>Anderen concentreerden zich meer op het satanische imago
            van Mayhem en de snelle muziek van Immortal. Zo snel spelen als je kunt werd het kenmerk van bands
            als Dark Funeral, Marduk en Gorgoroth, soms ook ironisch norsecore genoemd. Een nieuwe belangrijke
            stroming werd de symfonische black metal. Vooral geïnspireerd door de sound van Emperor maken zij
            black metal met sfeervolle keyboards, zoals Dimmu Borgir.
         </p>
         <p>Voor sommige bands werd satanisme te cliché gevonden; thema's als nationalisme, folklore, racisme en
            fascisme werden nu met black metal gecombineerd. Deze stroming kent vooral aanhang in Oost-Europa en
            luistert naar de naam NSBM (national socialist black metal). De teksten zijn niet altijd openlijk
            racistisch, vaak schuilt het pro-Arische en nationalistische gedachtegoed in dubbelzinnige teksten.
            Bekende bands zijn Graveland en Veles uit Polen; Forest en Branikald uit Rusland; Absurd en
            Bilskirnir uit Duitsland en Kristallnacht uit Frankrijk. Muzikaal is er nauwelijks verschil met
            black metal (hoewel sommige NSBM-bands meer richting RAC en nazi-punk neigen).
         </p>
         <p>Nog een andere substijl is de pagan black metal. Ook deze bands concentreren zich op de eigen
            cultuur en in de teksten vinden we dan ook veel oude sagen, legendes en andere folklore terug. De
            muziek wordt begeleid door synthesizers of traditionele instrumenten zoals fluiten en hoorns.
            Voorbeelden zijn Cyhiriaeth, Eald en Dhaubgurz uit Spanje; Pagan Reign en Temnozor uit Rusland;
            Nokturnal Mortum en Kroda uit Oekraïne en Surturs Lohe uit Duitsland.
         </p>
         <p>De Amerikaanse scene was al lange tijd bekend door bands als Absu, Judas Iscariot en Sarcophagus.
            Rond de eeuwwisseling kregen steeds meer bands uit Amerika bekendheid. Zij spelen een rauwe maar
            melodieuze vorm van black metal. Voorbeelden zijn Krieg, Weltmacht en Averse Sefira. Al snel werd de
            term USBM ('United States black metal') gebruikt om deze bands aan te duiden. De muziek van Xasthur
            was langzamer en leek daardoor op doommetal: langzame keyboards en hysterisch schreeuwen kenmerken
            de sfeer. Omdat Xasthur zo verschilde van de USBM en andere black metal werd ze aangeduid als
            suicidal black metal. Deze noemer werd in 2003 populair waardoor alsnog bands hieronder werden
            geschaard, zoals Abyssic Hate, Forgotten Woods en Nortt. Andere bands uit deze substijl zijn de
            Nederlandse Nibdem en het Zweedse Shining.
         </p>
         <p>Tot slot is er nog een stroming die op het eerste gehoor vrij weinig te maken heeft met black metal.
            In deze stroming, soms "black noise" genaamd, vanwege de invloeden uit de noisemuziek, gaat het niet
            om ritmes of melodieën maar om de textuur van het geluid. De klassieke black metalinstrumentatie
            wordt hierbij gecombineerd met elektronica en/of hard ongestructureerd geschreeuw om een zo
            ziekelijk mogelijke sound te creëren. Abruptum uit Zweden is een pionier in het genre, die ook al in
            de tweede golf actief was. Andere bands zijn het Nederlandse Stalaggh en de Duitse Sadistic
            Penetration.
         </p>
      </div>
   </div>
</div>
<div data-role="page" id="kenmerken">
   <div data-
