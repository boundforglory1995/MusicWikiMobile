<div data-role="page" id="critic">
   <div data-role="header">
      <a href="#dialogPage_7" data-icon="back"></a>
      <h1>Criticism</h1>
   </div>
   <div data-role="content">
      <div class="verplaatsing">
         <p>Deathcore has often been criticized or looked down upon, especially by longtime fans of other genres of heavy metal. The reason for it is often due to its fusion of death metal and metalcore and use of breakdowns. </p>
         <p>In an interview with Vincent Bennett from The Acacia Strain about the deathcore label, he said "Deathcore is the new nu-metal. [...] It sucks. And if anyone calls us 'deathcore' then I might do something very bad to them. While in an interview with Justin Longshore from Through the Eyes of the Dead about the deathcore label, he said "You know, I really hate that term. I know we've been labeled as that but I think there's so much more to our music than just a mixture of death metal and hardcore (sic) even though we incorporate those elements in our music. To me it seems that is just the new and fresh thing that kids are following." </p>
         <p>In November 2013, Terrorizer wrote "The term ‘deathcore’ is usually seen as a dirty word in metal circles" whilist interviewing vocalist Bryce Lucien of the Texan-based metal band Seeker. Lucien then stated: </p>
         <p>Much like what became of metalcore in the mid-2000s, deathcore is an often maligned term that can instantly diminish a bands credibility. What once conjured images of ridiculously brutal, unapologetically heavy bands like Ion Dissonance and The Red Chord now brings to mind bands full of twenty year olds sporting throat tattoos, matching black t shirts, and trying desperately hard to look tough while they jump in sync onstage [...] My band is constantly referred to as deathcore. I personally don’t hear it at all, but that’s fine. I know where we pull our influence from.</p>
         <p>In contradiction however, Scott Lewis of the San Diego-based deathcore band Carnifex seemed more lighthearted and less concerned over the label than most musicians in the genre by stating "We're not one of those bands trying to escape the banner of deathcore. I know a lot of bands try and act like they have a big problem with that, but if you listen to their music, they are very 'deathcore.' I know that there is a lot of resentment towards deathcore and kind of younger bands." </p>
      </div>
