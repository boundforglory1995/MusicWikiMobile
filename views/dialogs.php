      <h1>Black Metal Kenmerken</h1>
   </div>
   <div data-role="content" >
      <div class="verplaatsing">
         <p>Muzikaal kent black metal een grote
            diversiteit: van snel tot langzaam, extreem minimalistisch tot veel
            tempoveranderingen. Weinig genres in de metal zijn zo ongedefinieerd
            en toch gebonden aan diverse concrete grenzen. De bezetting van een
            black-metalband is veelal gelijk aan die van andere genres: een
            drummer, één of twee gitaristen, een basgitarist en een vocalist.
            Daarnaast hebben diverse bands ook nog een toetsenist en eventuele
            andere instrumenten. De drumcomputer komt ook vaak voor, vooral bij
            eenmansbands.
         </p>
         <p>Standaard in de black metal zijn de schelle vocalen die niet zozeer
            gezongen worden, maar meer gelijkenis tonen met gekrijs. Veel minder
            tot nauwelijks gebruikelijk zijn de lage, gutturale vocalen zoals de
            grunt uit de deathmetal. Het gitaarwerk is meestal monotoon en bestaat
            vaak uit het herhalen van enkele riffs of akkoorden. Tegelijkertijd
            zijn er ook bands die een groot assortiment aan riffs bieden,
            bijvoorbeeld Emperor. Het drumwerk is meestal minder belangrijk,
            dubbele basdrum en blastbeats vormen veelal de ruggengraat van de
            muziek. De basgitaar volgt vaak de gitaarriffs of het drumpatroon en
            is niet altijd te horen, zeker niet bij blackmetalgroepen die zweren
            bij een ondermaatse, opzettelijke slechte productie. Daarnaast
            gebruiken sommige bands voor de sfeer nog keyboards terwijl andere
            bands dit niet vinden passen bij de algehele sfeer van black metal.
         </p>
         <p>Grofweg is black metal onder andere herkenbaar aan:
            schreeuwende vocalen;
            Gitaarspel met tremelo-techniek;
            snelle percussie met behulp van dubbele basdrum en blastbeats;
            relatief dik of zacht gitaargeluid, zit er zelden tussenin, met distortion;
            overwegend satanistische, misantropische, heidense en/of anti-christelijke thematiek;
            onconventionele songstructuren: vaak niet opgebouwd volgens de typische vers-refrein-vers-refrein-structuur.
         </p>
      </div>
   </div>
