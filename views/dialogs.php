<div data-role="page" id="blacktwee">
   <div data-role="header">
      <a
         href="#dialogPage_5" data-icon="back"></a>     
      <h1>Black Metal Tweede Golf</h1>
   </div>
   <div data-role="content" >
      <div class="verplaatsing">
         <p>Begin jaren
            90 ontstond de tweede golf waarin black metal uitkristalliseerde tot een
            zelfstandig genre. Vooral in Noorwegen werd het genre verder gevormd. In die
            tijd was deathmetal populair, terwijl velen het verschil tussen black en
            deathmetal niet duidelijk is. Veel blackmetalbands uit deze golf speelden eerst
            deathmetal, zoals Darkthrone (op het album Soulside Journey), Immortal (die
            ontstond uit de deathmetalbands Amputation en Old Funeral) en Emperor (destijds
            Thou Shalt Suffer geheten). Zij werden allen door Mayhem-gitarist Euronymous
            overtuigd om black metal te spelen.
         </p>
         <p>Het grootste verschil tussen de twee stijlen
            is dat black metal zich niet houdt aan conventionele songstructuren, terwijl de
            deathmetal van begin jaren 90 nog veelal was opgebouwd volgens een vers-refrein-
            vers-solo-vers-refrein-structuur.
         </p>
         <p> Er werd weinig aandacht aan het drumwerk
            geschonken, sing-along refreinen waren uit den boze, laat staan solo's. Kortom,
            veel eenvoudige riffs die eindeloos herhaald werden, originaliteit, creativiteit
            en minimalisme waren belangrijk voor de vorming van black metal. Corpse-paint
            werd een vast bestanddeel van de stroming: de grime onderstreepte het donkere
            imago van black metal en was een belangrijk element tijdens de live shows.
            Abbath, frontman van Immortal, spreekt over het naar buiten brengen van zijn
            innerlijke demonen.
         </p>
         <p>De blackmetalbands schoten als paddenstoelen uit de grond, met als belangrijkste
            groepen: Mayhem, Burzum (een eenmansproject van Varg Vikernes, ook Mayhem-
            bassist voor het De Mysteriis Dom Sathanas album), Darkthrone (de band die naast
            Burzum minimalisme doorvoerde), Immortal (gekenmerkt door hun obsessie met ijs
            en kou en Abbath's typerende raspende stemgeluid) en Emperor die met veel
            orkestrale keyboard-arrangementen de trend voor de symfonische black metal
            zette.
         </p>
         <p>Enslaved uit Noorwegen hoort in eerste instantie ook bij deze golf. Zij
            worden vaak bestempeld als vikingmetal, waarvan zij zelf ook deels de bedenkers
            waren. De muziek van hun eerste twee demo's was echter overwegend black metal
            met teksten over de Vikingcultuur en heidense mythologie. In landen als Polen,
            Duitsland, en Frankrijk doken ook blackmetalbands op met een frisse, nieuwe
            klank. De belangrijkste waren: Graveland en Perunwit uit Polen, Moonblood uit
            Duitsland en uit Frankrijk kwamen Les Legions Noires bestaande uit Belketre,
            Mütiilation, Vlad Tepes, Torgeist, Brenoritvrezorkre en hun diverse
            nevenprojecten.
         </p>
         <p>Black metal bleef een underground-verschijnsel. Dit veranderde toen in Noorwegen
            grafschendingen en brandstichtingen in kerken werden gepleegd. In 1993 werd Varg
            Vikernes opgepakt wegens brandstichting van drie kerken en de moord op
            medebandlid Eurynomous uit Mayhem. Ook werden leden uit Immortal en Emperor
            opgepakt wegens betrokkenheid bij de kerkbranden. Emperor kwam in 1995 weer in
            de schijnwerpers te staan nadat bekend werd dat drummer Bard "Faust" Eithun een
            homoseksuele man had doodgestoken.
         </p>
